void
togglescratch(const Arg *arg)
{
	Client *c;
	unsigned int found = 0;
	unsigned int scratchtag = SPTAG(arg->ui);
	Arg sparg = {.v = scratchpads[arg->ui].cmd};

	for (c = selmon->cl->clients; c && !(found = c->tags & scratchtag); c = c->next);
	if (found) {
		unsigned int newtagset = selmon->tagset[selmon->seltags] ^ scratchtag;
		if (newtagset) {
			selmon->tagset[selmon->seltags] = newtagset;
			focus(NULL);
			arrange(selmon);
		}
		if (ISVISIBLE(c, selmon)) {
			focus(c);
			restack(selmon);
		}
	} else {
		selmon->tagset[selmon->seltags] |= scratchtag;
		spawn(&sparg);
	}
}

void
toggletag(const Arg *arg)
{
	Monitor *m;
	unsigned int newtags;

	if (!selmon->sel)
		return;
	newtags = selmon->sel->tags ^ (arg->ui & TAGMASK);
	if (newtags) {
		/* prevent adding tags that are in use on other monitors */
		for (m = mons; m; m = m->next)
			if (m != selmon && newtags & m->tagset[m->seltags])
				return;
		selmon->sel->tags = newtags;
		focus(NULL);
		arrange(selmon);
	}
	updatecurrentdesktop();
}

int
findfirstunusedtag(void)
{
	Monitor *m;
	int occupied = 0;

	for (m = mons; m; m = m->next)
		occupied |= m->tagset[m->seltags];

	for (int i = 1; i < occupied; i <<= 1) {
		if ((i & occupied) == 0)
			return i;
	}
	return 0;
}

void
toggleview(const Arg *arg)
{
	Monitor *m;
	unsigned int newtagset = selmon->tagset[selmon->seltags] ^ (arg->ui & TAGMASK);

	/* prevent displaying the same tags on multiple monitors */
	for(m = mons; m; m = m->next) {
		if(m != selmon && newtagset & m->tagset[m->seltags]) {
			m->tagset[m->seltags] = m->tagset[m->seltags] ^ (arg->ui & TAGMASK);
			if (!m->tagset[m->seltags]) {
				m->tagset[m->seltags] |= findfirstunusedtag();
			}
			m->sel = NULL;
			attachclients(m);
			arrange(m);
		}
	}
	selmon->tagset[selmon->seltags] = newtagset;
	attachclients(selmon);
	arrange(selmon);
	focus(NULL);
	updatecurrentdesktop();
}

