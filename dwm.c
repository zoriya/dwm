void
updatebarpos(Monitor *m)
{
	m->wy = m->my;
	m->wh = m->mh;
	int num_bars;
	Bar *bar;
	int y_pad = 0;
	int x_pad = 0;

	for (bar = m->bar; bar; bar = bar->next) {
		bar->bx = m->mx + x_pad;
		bar->bw = m->ww - 2 * x_pad;
		bar->bh = bh;
	}

	if (!m->showbar) {
		for (bar = m->bar; bar; bar = bar->next)
			bar->by = -bh - y_pad;
		return;
	}

	for (num_bars = 0, bar = m->bar; bar; bar = bar->next, num_bars++)
		if (bar->topbar)
			m->wy = m->my + bh + y_pad;
	m->wh = m->wh - y_pad * num_bars - bh * num_bars;

	for (bar = m->bar; bar; bar = bar->next)
		bar->by = (bar->topbar ? m->wy - bh : m->wy + m->wh);
}

